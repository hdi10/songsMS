CREATE TABLE SongLists(SongListsId INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1 INCREMENT BY 1) PRIMARY KEY, OwnerId VARCHAR(20) NOT NULL REFERENCES Users(UserId), Name VARCHAR(50) NOT NULL, IsPrivate BOOLEAN NOT NULL);

CREATE TABLE contains(SongListsId INTEGER REFERENCES SongLists(SongListsId), SongId INTEGER REFERENCES Song(SongId));
CREATE TABLE has (UserID VARCHAR(20) REFERENCES Users(UserId), SongListsId INTEGER REFERENCES SongLists(SongListsId));

